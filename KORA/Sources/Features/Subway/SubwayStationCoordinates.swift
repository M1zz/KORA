    /// Nearest station (by great-circle distance) to a given lat/lng,
    /// within `maxMeters` (default 2km). Returns the canonical Korean
    /// station name, e.g. "강남", or nil if nothing is close enough.
    static func nearestStation(latitude lat: Double, longitude lng: Double, maxMeters: Double = 2000) -> (name: String, distanceMeters: Double)? {
        var best: (String, Double)? = nil
        for (name, c) in stationCoordinates {
            let d = haversine(lat1: lat, lng1: lng, lat2: c.lat, lng2: c.lng)
            if d > maxMeters { continue }
            if best == nil || d < best!.1 { best = (name, d) }
        }
        guard let b = best else { return nil }
        return (b.0, b.1)
    }

    /// Great-circle distance in meters between two WGS84 coordinates.
    private static func haversine(lat1: Double, lng1: Double, lat2: Double, lng2: Double) -> Double {
        let R = 6_371_000.0
        let φ1 = lat1 * .pi / 180
        let φ2 = lat2 * .pi / 180
        let dφ = (lat2 - lat1) * .pi / 180
        let dλ = (lng2 - lng1) * .pi / 180
        let a = sin(dφ/2) * sin(dφ/2) + cos(φ1) * cos(φ2) * sin(dλ/2) * sin(dλ/2)
        let c = 2 * atan2(sqrt(a), sqrt(1 - a))
        return R * c
    }
